class OuterClass:
    @staticmethod
    def ifElse(a):
        if a > 10:
            print(">10")
        elif a > 8:
            print(">8")
        elif a > 6:
            print(">6")
        elif a > 4:
            print(">4")
        elif a > 2:
            print(">2")
        else:
            print("<=2")